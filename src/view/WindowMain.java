    private void potvrdiRezervacijuDugmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_potvrdiRezervacijuDugmeActionPerformed

        DefaultTableModel modelZ = (DefaultTableModel) tabelaKorisnikZaduzenja.getModel();
        DefaultTableModel modelR = (DefaultTableModel) tabelaKorisnikRezervacije.getModel();

        int row = tabelaKorisnikRezervacije.getSelectedRow();
        int column = 0;
        String invBroj = String.valueOf(tabelaKorisnikRezervacije.getModel().getValueAt(row, column));
        Book k = dbb.findBookToBorrow(invBroj);
        Long userID = Long.valueOf(korisnickiBroj.getText());

        dbb.zaduziKorisnik(userID, k);

        try{
            boolean ponisti = dbb.ponistiRezervaciju(invBroj, userID);
            if (ponisti)
                infoBox("Rezervacija ponistena", "korisnik");
            else
                infoBox("Greska", "rezervacija");
        } catch (Exception e){
            System.out.println(e);
        }

        ArrayList<String> dates = getDates();
        String datumZaduzenja = dates.get(0);
        String datumRazduzenja = dates.get(1);

        modelZ.addRow(new Object[]{k.getInventoryNum(), k.getProperTitle(), k.getFirstStmntResp(), datumZaduzenja, datumRazduzenja});
        modelR.removeRow(row);
    }//GEN-LAST:event_potvrdiRezervacijuDugmeActionPerformed

