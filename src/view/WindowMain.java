    // Metod za dobijanje potrebnih datuma
    public ArrayList<String> getDates(){
        ArrayList<String> dates = new ArrayList<String>();
        
        Calendar cal = Calendar.getInstance();
        Date bookTake = cal.getTime();
        cal.add(Calendar.MONTH, 1);
        Date bookReturn = cal.getTime();
        cal.add(Calendar.MONTH, 11);
        
        Date memberExpire = cal.getTime();
        String datumDanas = String.valueOf(new SimpleDateFormat("dd.MM.yyyy").format(bookTake));        
        String datumRazduzenja = String.valueOf(new SimpleDateFormat("dd.MM.yyyy").format(bookReturn));
        String istekClanarine = String.valueOf(new SimpleDateFormat("dd.MM.yyyy").format(memberExpire));
        
        dates.add(datumDanas);
        dates.add(datumRazduzenja);
        dates.add(istekClanarine);
        return dates;
    }

