    private void sacuvajKorisnikaDugmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sacuvajKorisnikaDugmeActionPerformed
        
        String name = korisnikImeUnos.getText();
        String lastName = korisnikPrezimeUnos.getText();
        String parent = korisnikRoditeljUnos.getText();
        String document = korisnikDokumentUnos.getText();
        Long documentID = Long.valueOf(korisnikBrojDokumentaUnos.getText());
        String joinType = korisnikVrstaUclanjenjaUnos.getText();
        String birthDate = korisnikDatumRodjenjaUnos.getText();
        Long userID = Long.valueOf(korisnikClanskiBrojUnos.getText());
        Long price = Long.valueOf(cenaKorisnikUnos.getText());

        // Preuzimanje datuma iz funkcije getDates() na kraju ovog fajla
        ArrayList<String> dates = getDates();
        String joinDate = dates.get(0);
        String expireDate = dates.get(2);
        
        boolean inserted = dbb.insertKorisnik(name, lastName, parent, document, documentID, joinType, price, birthDate, userID, joinDate, expireDate);
        if (inserted) 
            infoBox("User je uspesno ubacen!", "Sacuvaj korisnika");
        else
            infoBox("Greska u cuvanju korisnika!", "Sacuvaj korisnika");
    }//GEN-LAST:event_sacuvajKorisnikaDugmeActionPerformed

